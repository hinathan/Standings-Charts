function paletteWithEntries(length) {
	if(length > 9) {
		var pull_from = 8;
		var composite = brewer_palettes[pull_from];
		while(composite.Set1.length < length) {
			for(i in composite) {
				for(j in brewer_palettes[pull_from][i]) {
					if(brewer_palettes[pull_from][i].hasOwnProperty(j)) {
						composite[i].push(brewer_palettes[pull_from][i][j]);
					}
				}
			}
		}
		return composite;
	}
	return brewer_palettes[length];
}

